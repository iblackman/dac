    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String[] resChecked = request.getParameterValues("recursos");
        List<Resources> resources = new ArrayList<>();
        for(String id : resChecked){
            Resources res = new ResourcesDAO().findById(Integer.parseInt(id));
            resources.add(res);
        }
        int aux = Integer.parseInt(request.getParameter("roomId"));
        Rooms room = new RoomsDAO().findById(aux);
        
        room.setResourcesCollection(resources);
        new RoomsDAO().save(room);
        
        request.getRequestDispatcher("admRoom").forward(request, response);
    }

